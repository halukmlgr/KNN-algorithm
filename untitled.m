% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
load ClimateModel.mat; %data dosyası yükleniyor.
data = table2cell(ClimateModel);
set(handles.uitable1,'Data',data);
options.k = str2double(get(handles.edit7,'String')); % kullanıcıdan k değeri alınıyor
options.fold = 8;% çapraz doğrulama yapılırken kaçar kaçar alınacağı belirlendi. 
model = KNN(ClimateModel, options);% model değişkeninde data KNN de işleme sokuluyor.
%deger1 ile 6 arasında kullanıcı test verisi giriyor. 
deger1= str2double(get(handles.edit1,'String'));
deger2= str2double(get(handles.edit2,'String'));
deger3= str2double(get(handles.edit3,'String'));
deger4= str2double(get(handles.edit4,'String'));
deger5= str2double(get(handles.edit5,'String'));
deger6= str2double(get(handles.edit6,'String'));
%MDL değişkeninde knn algoritması uygulanıyor.
Mdl = fitcknn(model.Model.X, model.Model.Y, 'NumNeighbors', options.k, 'Distance', 'euclidean');
%yapılan işlemler de çıktı sonuc değişkenine aktarılıyor
sonuc = predict(Mdl, [deger1,deger2,deger3,deger4,deger5,deger6]);
%sonuc değişkeni yazdırılıyor
set(handles.text10,'String',sonuc);


